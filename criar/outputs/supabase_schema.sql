-- SECURITY FIX: policies used to also allow access via
-- `phone = public.current_user_phone()`. Since `phone` is a free-text field
-- customers can edit on their own profile (see profiles_update_own_customer),
-- anyone could set their phone to match another customer's and read/insert
-- into their order history. Authorization now relies only on `user_id`,
-- which is set by Supabase Auth and cannot be forged by the client.
drop policy if exists "orders_select_own_or_staff" on public.orders;
drop policy if exists "orders_insert_own" on public.orders;
drop policy if exists "orders_insert_staff" on public.orders;
drop policy if exists "orders_update_staff_only" on public.orders;
drop policy if exists "orders_delete_admin_only" on public.orders;
create policy "orders_select_own_or_staff"
  on public.orders for select
  using (public.is_staff() or user_id = auth.uid());
create policy "orders_insert_own"
  on public.orders for insert
  with check (
    auth.uid() is not null
    and user_id = auth.uid()
  );
create policy "orders_insert_staff"
  on public.orders for insert
  with check (public.is_staff());
create policy "orders_update_staff_only"
  on public.orders for update
  using (public.is_staff())
  with check (public.is_staff());
create policy "orders_delete_admin_only"
  on public.orders for delete
  using (public.current_user_role() in ('admin', 'super_admin'));

