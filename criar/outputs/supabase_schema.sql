create or replace function public.set_updated_at()
returns trigger as $$
begin
  new.updated_at = now();
  return new;
end;
$$ language plpgsql;

create or replace function public.current_user_role()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select coalesce((select role from public.profiles where id = auth.uid()), 'anonymous')
$$;

create or replace function public.current_user_phone()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select coalesce((select phone from public.profiles where id = auth.uid()), '')
$$;

create or replace function public.is_staff()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select public.current_user_role() in ('employee', 'delivery', 'admin', 'super_admin')
$$;

create or replace function public.validate_order_totals()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  item jsonb;
  product_row public.products%rowtype;
  requested_id text;
  requested_qty integer;
  normalized_items jsonb := '[]'::jsonb;
  subtotal numeric(10,2) := 0;
  config public.store_settings%rowtype;
begin
  select * into config
  from public.store_settings
  where id = 'default';

  if config.id is null then
    raise exception 'Configuração da loja não encontrada';
  end if;

  if new.items is null or jsonb_typeof(new.items) <> 'array' or jsonb_array_length(new.items) = 0 then
    raise exception 'Pedido sem itens';
  end if;

  for item in select * from jsonb_array_elements(new.items)
  loop
    requested_id := item ->> 'id';
    if requested_id is null or requested_id = '' then
      raise exception 'Item sem produto';
    end if;

    if coalesce(item ->> 'qty', '') !~ '^[0-9]+$' then
      raise exception 'Quantidade inválida para o produto %', requested_id;
    end if;

    requested_qty := (item ->> 'qty')::integer;

    if requested_qty < 1 or requested_qty > 99 then
      raise exception 'Quantidade inválida para o produto %', requested_id;
    end if;

    select * into product_row
    from public.products
    where id = requested_id and available = true;

    if product_row.id is null then
      raise exception 'Produto indisponível ou inexistente: %', requested_id;
    end if;

    if product_row.stock < requested_qty then
      raise exception 'Estoque insuficiente para o produto %', requested_id;
    end if;

    subtotal := subtotal + (product_row.price * requested_qty);
    normalized_items := normalized_items || jsonb_build_array(jsonb_build_object(
      'id', product_row.id,
      'name', product_row.name,
      'price', product_row.price,
      'qty', requested_qty
    ));
  end loop;

  if subtotal < config.minimum_order then
    raise exception 'Pedido mínimo não atingido. Mínimo: R$ %', config.minimum_order;
  end if;

  new.items := normalized_items;
  if new.delivery_mode = 'Retirada no local' then
    new.delivery_fee := 0;
  else
    new.delivery_fee := config.delivery_fee;
  end if;

  return new;
end;
$$;

create or replace function public.decrement_stock_after_order()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  item jsonb;
  requested_id text;
  requested_qty integer;
begin
  for item in select * from jsonb_array_elements(new.items)
  loop
    requested_id := item ->> 'id';
    requested_qty := (item ->> 'qty')::integer;

    update public.products
    set stock = stock - requested_qty,
        updated_at = now()
    where id = requested_id;

    if not found then
      raise exception 'Produto não encontrado para descontar estoque: %', requested_id;
    end if;
  end loop;

  return new;
end;
$$;

